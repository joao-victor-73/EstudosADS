-- 1. RELATÓRIO GERAL DE TODOS OS CLIENTES 
DESC endereco;
DESC cliente;
DESC telefone;

SELECT c.*, e.rua, e.bairro, e.cidade, e.estado, t.tipo, t.numero
FROM cliente c
	INNER JOIN endereco e
    ON c.idcliente = e.fk_idcliente
		INNER JOIN telefone t
        ON c.idcliente = t.fk_idcliente;
        

